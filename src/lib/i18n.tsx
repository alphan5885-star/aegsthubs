const translations = { tr } as const;

export type TranslationKey = keyof typeof tr;

interface I18nContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: TranslationKey | string) => string;
}

const I18nContext = createContext<I18nContextType>({
  language: "tr",
  setLanguage: () => {},
  t: (key) => String(key),
});

export const useI18n = () => useContext(I18nContext);

const LANG_KEY = "app_language";

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window === "undefined") return "tr";
    try {
      const stored = window.localStorage.getItem(LANG_KEY) as Language;
      return stored && ["tr", "en", "ru"].includes(stored) ? stored : "tr";
    } catch {
      return "tr";
    }
  });

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    try { localStorage.setItem(LANG_KEY, lang); } catch {}
    if (typeof document !== "undefined") document.documentElement.lang = lang;
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") document.documentElement.lang = language;
  }, [language]);

  const t = useCallback(
    (key: TranslationKey | string): string => {
      const dict = translations.tr as Record<string, string>;
      return dict[key as string] || String(key);
    },
    [],
  );

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>{children}</I18nContext.Provider>
  );
}

